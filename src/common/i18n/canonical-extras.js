export const CANONICAL_EXTRA_COPY_BY_LOCALE = Object.freeze({
	en: Object.freeze(Object.fromEntries(COPY_ENTRIES.map(([key, en]) => [key, en]))),
	de: Object.freeze(Object.fromEntries(COPY_ENTRIES.map(([key, , de]) => [key, de]))),
});

function keyPart(value) {
	return String(value).split(/[^a-zA-Z0-9]+/u).filter(Boolean)
		.map((part) => part[0].toUpperCase() + part.slice(1)).join('');
}

export function effectNameCopyKey(type) {
	return 'effectName' + keyPart(type);
}

export function effectParameterCopyKey(type, name) {
	return 'effectParam' + keyPart(type) + keyPart(name);
}

export function effectOptionCopyKey(type, name, value) {
	return 'effectOption' + keyPart(type) + keyPart(name) + keyPart(value);
}

export function effectCardCopyKey(title) {
	return 'effectCard' + keyPart(title);
}

export function audacityParityReasonCopyKey(reason) {
	return 'audacityParityReason' + keyPart(reason);
}

export function audacityParityLabelCopyKey(label) {
	return 'audacityParityLabel' + keyPart(label);
}

export function canonicalCopyValue(key, copyOrLocale = 'en') {
	const copy = copyOrLocale && typeof copyOrLocale === 'object'
		? copyOrLocale
		: CANONICAL_EXTRA_COPY_BY_LOCALE[localeLanguage(copyOrLocale)] || CANONICAL_EXTRA_COPY_BY_LOCALE.en;
	return copy[key] ?? CANONICAL_EXTRA_COPY_BY_LOCALE.en[key] ?? key;
}
